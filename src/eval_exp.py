class Stats:
  def __init__(self, L = None):
    if L is None:
      L = []
    self.n = len(L)
    self.sum = sum(L)
    self.sumsqr = sum(x * x for x in L)
    self.min = None
    self.max = None
    if L:
      S = sorted(L)
      self.median = S[len(S) // 2]
      self.quart = S[len(S) // 4]
      self.min = min(L)
      self.max = max(L)

  def update(self, L):
    self.n += len(L)
    self.sum += sum(L)
    self.sumsqr += sum(x * x for x in L)
    if self.min is not None:
      self.min = min(self.min, min(L))
    else:
      self.min = min(L)
    if self.max is not None:
      self.max = max(self.max, max(L))
    else:
      self.max = max(L)
    self.median = None
    self.quart = None
   
  def avg(self):
    return self.sum / self.n

  def var(self):
    return (self.sumsqr - self.sum **2 / self.n) / (self.n - 1)

  def std_dev(self):
    return math.sqrt(self.var())

  def cv(self):
    return self.std_dev() / self.avg()

  def indexOfDispersion(self):
    return self.var() / self.avg()

  def description(self):
    return 'samples:%d avg:%s std:%s, cv:%s iod:%s' % (self.n, self.avg(),
        self.std_dev(), self.cv(), self.indexOfDispersion()) 

  __str__ = description

def parse_res(res: str):
  for line in res.split('\n'):
    if not line: continue
    tc, a, b, r = line.split(' ')
    assert r in ('PublicAIsRight', 'PublicBIsRight')
    tcid = int(tc.split(':')[1])
    aIsFaster = int(a.split(':')[1])
    bIsFaster = int(b.split(':')[1])
    yield tcid, aIsFaster, bIsFaster, r == 'PublicAIsRight'
  
def eval_exp(res: str, mindiff=1000):
  skipped = 0
  correct = 0
  incorrect = 0
  for tcId, a, b, first in parse_res(res):
    diff = a - b
    if abs(diff) < mindiff:
      skipped += 1
    elif (diff > 0) == first:
      correct += 1
    else:
      incorrect += 1
  print(mindiff, skipped, correct, incorrect)

