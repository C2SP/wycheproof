def get_test_key(size: int,
                 md: Optional[str] = None,
                 three_primes: bool = False,
                 log: bool = True,
                 generate_new: bool = False):
  if three_primes:
    assert not md
    for k in rsa_three_primes_keys:
      if k.n.bit_length() == size:
        k.fill_crt()
        return k
    if generate_new:
      newkey = rsa_key.new_three_prime_rsa_key(size, e=65537)
      newkey.fill_crt()
      rsa_three_primes_keys.append(newkey)
      return newkey
    else:
      raise ValueError("No precomputed 3prime key available for"
                       " size:%d, md:%s" % (size, md))

  if md is None:
    # Searches an encryption key
    for k in rsa_pkcs1_keys:
      if k.n.bit_length() == size and k.md == md:
        k.fill_crt()
        return k
  else:
    # Searches a signature key
    for k in rsa_keys:
      if k.n.bit_length() == size and k.md == md:
        k.fill_crt()
        return k
  if generate_new:
    newkey = rsa_key.newRsaKey(size, e=65537, md=md)
    newkey.fill_crt()
    if log:
      md_name= md_lower(md)
      print("rsa_key_%d_%s = %s" % (size, md_name, str(newkey)))
    rsa_signature_keys.append(newkey)
    return newkey
  else:
    raise ValueError("No precomputed Rsa key available for"
                       " size:%d, md:%s" % (size, md))

