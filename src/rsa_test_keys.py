def all_private_keys():
  for n, val in globals().items():
    if isinstance(val, RsaPrivateKey):
      yield n, val

def md_lower(md: str) -> str:
  """Converts a name of a message digest into a lower case variable name.

  Args:
    md: the name of the message digest
  Returns:
    a lower case representation of the message digest.
    E.g. 'SHA-1' -> 'sha1'
         'SHA3-256' -> 'sha3_256'
         'SHA-512/224' -> 'sha512_224'
  """
  md = md.lower()
  md = md.replace('sha-', 'sha')
  md = md.replace('-', '_')
  return md.replace('/', '_')

def gen_new_keys(params, key_type, e=65537, three_primes=False):
  print(params, three_primes)
  keylist = []
  for size, md in params:
    if three_primes:
      prefix = "rsa_three_prime"
    else:
      prefix = "rsa"
    if md:
      shortmd = md_lower(md)
      keyid = prefix + '_key_%d_%s'%(size, shortmd)
    else:
      keyid = prefix + '_key_%d'%size
    if three_primes:
      key = rsa_key.new_three_prime_rsa_key(size, e=e)
    else:
      key = rsa_key.newRsaKey(size, e=e, md=md)
    key.id = keyid
    print(keyid + ' = ' + str(key))
    keylist.append(keyid)
  print("rsa_%s_keys = [%s]" % (key_type, ', '.join(keylist)))

