import poly
import xdh

def XdhPoly(group: xdh.Xdh, k:int):
  def cswap(bit, x, y):
    if bit:
      return y, x
    else:
      return x, y

  p = group.p
  a24 = group.a24
  u = poly.Polynomial([0, 1], p)
  x_1 = u
  x_2 = 1
  z_2 = 0
  x_3 = u
  z_3 = 1
  swap = 0

  bits = k.bit_length()
  for t in range(bits - 1, -1, -1):
    k_t = (k >> t) & 1
    swap ^= k_t
    x_2, x_3 = cswap(swap, x_2, x_3)
    z_2, z_3 = cswap(swap, z_2, z_3)
    swap = k_t
    A = (x_2 + z_2)   # Special point x == -z
    AA = A*A
    B = (x_2 - z_2)   # Special point x == z
    BB = B*B
    E = (AA - BB)     # Special point = 4*x^2*z^2
    C = (x_3 + z_3)
    D = (x_3 - z_3)
    DA = D*A
    CB = C*B
    x_3 = (DA + CB) * (DA + CB)
    z_3 = x_1 * (DA - CB) * (DA - CB)
    x_2 = AA*BB
    z_2 = E*(AA + a24*E)
  x_2, x_3 = cswap(swap, x_2, x_3)
  z_2, z_3 = cswap(swap, z_2, z_3)
  return [
    ('x_2', x_2),
    ('z_2', z_2),
    ('A', A),
    ('AA', AA),
    ('B', B),
    ('BB', BB),
    ('E', E),
    ('C', C),
    ('D', D),
    ('CB', CB),
    ('DA', DA),
    ('DA + CB', DA + CB),
    ('DA - CB', DA - CB),
  ]

def X25519Poly(k: int):
  return XdhPoly(xdh.x25519, k)

def X448Poly(k: int):
  return XdhPoly(xdh.x448, k)

def gen_special(group, maxk: int, values=None):
  if values is None:
    values = [-1, 0, 1,
              2**224,
              2**group.p.bit_length() % group.p, 
              2**(group.p.bit_length() - 1) - 1]
  found = set()
  res = {}
  for k in range(1, maxk+1):
    L = []
    for n, p in XdhPoly(group, k):
      if not isinstance(p, poly.Polynomial):
        continue
      for v in values:
        for r in (p - v).roots():
          if r not in found:
            if v == 0:
              comment = "%s = 0" % n
            else:
              comment = "special case for %s" % n
            found.add(r)
            L.append((r, comment))
    res[k] = sorted(L)
  return res

class Log:
  def __init__(self, val, logs):
    self.val = val
    self.logs = logs
  def __add__(self, other):
    assert isinstance(other, Log)
    assert other.logs == self.logs
    return Log(self.val + other.val, self.logs)
  def __sub__(self, other):
    return self + (-other)
  def __neg__(self):
    return Log(-self.val, self.logs)
  def __mod__(self, other):
    assert isinstance(other, int)
    return Log(self.val % other, self.logs)
  def __mul__(self, other):
    if isinstance(other, int):
       return Log(self.val * other, self.logs)
    else:
       assert isinstance(other, Log)
       logs = [x + y for x, y in zip(self.logs, other.logs)]
       return Log(self.val * other.val, logs)
  def __pow__(self, other):
    assert isinstance(other, int)
    assert 1 < other < 10
    res = self
    for i in range(1, other):
      res *= self
    return res

  __rmul__ = __mul__
  def __repr__(self):
    return 'Log(%s, %s)' % (self.val, repr(self.logs))
  __str__ = __repr__

def point_mult_rand_log(group, u:int, k:int):
    '''Simulates a randomized point multiplication and 
       keeps track of the exponent of the random int.
    '''
    def cswap(bit, x, y):
      if bit:
        return y, x
      else:
        return x, y

    p = group.p
    a24 = group.a24

    x_1 = Log(u, [0,0])
    x_2 = Log(1, [1,0])
    z_2 = Log(0, [1,0])
    z_3 = Log(1, [0,1])
    x_3 = u * z_3 % p
    swap = 0


    bits = k.bit_length()
    for t in range(bits - 1, -1, -1):
      k_t = (k >> t) & 1
      swap ^= k_t
      (x_2, x_3) = cswap(swap, x_2, x_3)
      (z_2, z_3) = cswap(swap, z_2, z_3)
      swap = k_t
      A = (x_2 + z_2) % p 
      AA = A*A % p
      B = (x_2 - z_2) % p
      BB = B*B % p
      E = (AA - BB) % p
      C = (x_3 + z_3) % p
      D = (x_3 - z_3) % p
      DA = D * A % p
      CB = C * B % p
      x_3 = (DA + CB)**2 % p
      z_3 = x_1 * (DA - CB)**2 % p
      x_2 = AA * BB % p
      z_2 = E * (AA + a24 * E) % p

    (x_2, x_3) = cswap(swap, x_2, x_3)
    (z_2, z_3) = cswap(swap, z_2, z_3)
    return x_2, z_2

