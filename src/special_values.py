  print('[')
  for md, L in EDGE_CASE_HASHES:
    print('  ("%s", (' % md)
    for t in sorted(L):
      b,p,m,h = t
      if len(h) <= 64:
        print('    %s,' % repr(t))
      else:
        print('    (%s, %s, %s,' % (b, p, repr(m)))
