def failures_experiment(fname='test.log'):
  txt = open(fname).read()
  lines = txt.split('\n')
  L = []
  for line in lines:
    parts = line.split(' ')
    key = None
    msg = None
    for p in parts:
      v = p.split(":", 1)
      if len(v) == 2:
        if v[0] == 'key':
          key = v[1]
        elif v[0] == 'msg':
          msg = v[1]
    if key and msg:
      L.append((key, msg))
  return L

def bias(tab):
    r = 0
    for i,c in enumerate(tab):
      w = 2*math.pi*i/len(tab)
      r += c*(math.cos(w) + 1j * math.sin(w))
    return abs(r)/sum(tab)

